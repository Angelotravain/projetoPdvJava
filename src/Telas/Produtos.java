    private void prodSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prodSalvarActionPerformed
        // TODO add your handling code here:
        prod.setNomeProd(prodNome.getText());
        prod.setDescProd(prodDescricao.getText());
        prod.setQuantProd(prodQuantidade.getText());
        prod.setValorProd(prodValor.getText().replace(",", "."));

        HibernateUtil.beginTransaction();
        HibernateUtil.getSession().merge(prod);
        HibernateUtil.commitTransaction();
        HibernateUtil.closeSession();

        prod = new Produto();
        limparCampos();
        montaTabela();
        validaCampos("inicio");
    }//GEN-LAST:event_prodSalvarActionPerformed

