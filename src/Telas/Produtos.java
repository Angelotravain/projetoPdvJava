    public void validaCampos(String operacao) {

        if (operacao == "inicio") {
            prodNome.setEnabled(false);
            prodDescricao.setEnabled(false);
            prodQuantidade.setEnabled(false);
            prodValor.setEnabled(false);
            prodExcluir.setEnabled(false);
            prodSalvar.setEnabled(false);
            prodCancelar.setEnabled(false);
            prodSair.setEnabled(true);
            prodNovo.setEnabled(true);
            prodAlterar.setEnabled(false);
        } else if (operacao == "novo") {
            prodNome.setEnabled(true);
            prodDescricao.setEnabled(true);
            prodValor.setEnabled(true);
            prodQuantidade.setEnabled(true);
            prodAlterar.setEnabled(false);
            prodExcluir.setEnabled(true);
            prodSalvar.setEnabled(true);
            prodCancelar.setEnabled(true);
        } else if (operacao == "selecionado") {

            prodNome.setEnabled(false);
            prodDescricao.setEnabled(false);
            prodValor.setEnabled(false);
            prodQuantidade.setEnabled(false);
            prodNovo.setEnabled(true);
            prodAlterar.setEnabled(true);
            prodExcluir.setEnabled(true);
            prodSalvar.setEnabled(false);
            prodCancelar.setEnabled(false);
            prodSair.setEnabled(true);
        }

    }

    public void montaTabela() {

        listaProdutos = HibernateUtil.getSession().createQuery("from Produto").list();

        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nome");
        modelo.addColumn("Quantidade estoque");
        modelo.addColumn("Valor");

        for (Produto produto : listaProdutos) {
            modelo.addRow(new Object[]{produto.getNomeProd(), produto.getQuantProd(), produto.getValorProd().replace(".", ",")});
        }

        tabelaProd.setModel(modelo);

    }

    public void limparCampos() {
        prodNome.setText("");
        prodDescricao.setText("");
        prodValor.setText("");
        prodQuantidade.setText("");
    }

