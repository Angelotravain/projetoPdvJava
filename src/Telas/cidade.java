    public void montaCombo() {
        listaEstados = HibernateUtil.getSession().createQuery("from Estado").list();
        comboEstado.removeAllItems();
        for (Estado e : listaEstados) {
            comboEstado.addItem(e.getNome_estado());
        }
    }

    public void validaTela(String operacao) {
        if (operacao.equals("inicio")) {
            nomeCidade.setEnabled(false);
            comboEstado.setEnabled(false);
            salvarCidade.setEnabled(false);
        } else if (operacao.equals("novo")) {
            nomeCidade.setEnabled(true);
            comboEstado.setEnabled(true);
            salvarCidade.setEnabled(true);
        } else if (operacao.equals("selecionado")) {
            nomeCidade.setEnabled(false);
            comboEstado.setEnabled(false);
            salvarCidade.setEnabled(false);
        }
    }

