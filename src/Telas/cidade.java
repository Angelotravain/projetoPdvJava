    private void salvarCidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarCidadeActionPerformed
        // TODO add your handling code here:
         cidade.setNome(nomeCidade.getText());
            cidade.setEstado(listaEstados.get(comboEstado.getSelectedIndex()));

            HibernateUtil.beginTransaction();
            HibernateUtil.getSession().merge(cidade);
            HibernateUtil.commitTransaction();
            HibernateUtil.closeSession();
            montaTabela();
    }//GEN-LAST:event_salvarCidadeActionPerformed
    private void montaTabela() {

        listaCidades = new ArrayList<Cidade>();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nome");
        modelo.addColumn("Estado");

        for (Cidade c : listaCidades) {
            modelo.addRow(new Object[]{c.getId(), c.getNome(), c.getEstado().getNome_estado()});
        }
        tabelaCidade.setModel(modelo);
    }


