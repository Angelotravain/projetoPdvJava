    private void salvarEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarEstadoActionPerformed
        // TODO add your handling code here: estado.setNome(cNome.getText());
        int contaSigla = siglaEstado.getText().length();
        if (contaSigla > 2) {
            JOptionPane.showMessageDialog(null, "A sigla tem mais de 2 caracteres, corrija e tente novamente!");
            siglaEstado.setText("");
            nomeEstado.setText("");
        } else {
            estado.setSigla_estado(siglaEstado.getText());
            estado.setNome_estado(nomeEstado.getText());
            HibernateUtil.beginTransaction();
            HibernateUtil.getSession().merge(estado);
            HibernateUtil.commitTransaction();
            HibernateUtil.closeSession();

            estado = new Estado();

            nomeEstado.setText("");
            siglaEstado.setText("");
            montaTabela();
        }
    }//GEN-LAST:event_salvarEstadoActionPerformed

