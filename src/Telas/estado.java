    public void montaTabela() {

        listaEstados = HibernateUtil.getSession().createQuery("from Estado").list();

        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nome");
        modelo.addColumn("Sigla");

        for (Estado est : listaEstados) {
            modelo.addRow(new Object[]{est.getNome_estado(), est.getSigla_estado()});
        }

        tabelaEstado.setModel(modelo);
    }

